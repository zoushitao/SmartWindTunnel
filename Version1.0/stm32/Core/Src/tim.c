// All fans execute the sin function
void pwm_sin(void){
	uint16_t t = 0;
	uint16_t DutyRation = 0;
	while(1){
		t = (t + 1) % Sampling_Time;
		HAL_Delay(1000 * T / Sampling_Time);
		HAL_GPIO_TogglePin(GPIOF, GPIO_PIN_9);
		HAL_GPIO_TogglePin(GPIOF, GPIO_PIN_10);
		DutyRation = Bias + (int)(Amplitude * sin(2* t * PI / Sampling_Time));
		for(int i = 0;i < 5;i++){
			for(int j = 0; j < 5; j++){
				PWM_config5[i][j] = DutyRation;
			}
		}
		PWM_SET();
	}
	
}
